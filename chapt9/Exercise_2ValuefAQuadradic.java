public class Exercise_2ValuefAQuadradic {
    public static void main(String[] args) {
    
      double X;
    
       X = 4.0;
       double result = 3 * X * X - 8 * X + 4;


       System.out.println("At X = " + X + " the value is " + result);

       X = 20;
       System.out.println("At X = " + X + " the value is " + result);

      
       X = (double)2/3;
       System.out.println("At X = " + X + " the value is " + result);

    }
}
